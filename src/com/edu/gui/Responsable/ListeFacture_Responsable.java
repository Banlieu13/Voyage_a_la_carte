    private void btnsuppMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnsuppMouseClicked
         int id;
        TableResponsableFacture to = new TableResponsableFacture();
           int x = jTable2.getSelectedRow();
           if(x==-1){
              msg.setText("Veuillez sélectioner une facture !!");
           }
           else
           {
           id = (int) to.getValueAt(x, 0);
           factureDAO f = new factureDAO();
           f.deleteFacture(id);
           jTable2.setModel(to);
           }
    }//GEN-LAST:event_btnsuppMouseClicked

