    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
           TableOffre to = new TableOffre();
           int x = jTable1.getSelectedRow();
           if(x==-1){
              msg.setText("Veuillez sélectioné une offre");
           }
           else
           {
           int id = (int) to.getValueAt(x, 0);
           System.out.println(id);
           new offreDAO().deleteOffre(id);
           to.listOffre = new offreDAO().DisplayAllOffre();
           jTable1.setModel(to);
           msg.setText("Suppression effectuée avec succée");
           }   
    }//GEN-LAST:event_jButton2ActionPerformed

