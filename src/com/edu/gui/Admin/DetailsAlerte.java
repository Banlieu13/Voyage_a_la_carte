    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       GererAlerteDAO gad = new GererAlerteDAO();
        int id = gad.getID();
        String l;
        String source = gad.getSource();
        if (source.compareTo("offre")==0){
          offreDAO o = new offreDAO();
          o.deleteOffre(id);
        } 
        if (source.compareTo("annonce")==0){
            AnnonceDAO a = new AnnonceDAO();
            a.deleteAnnonce(id);
        }
        if (source.compareTo("commentaire")==0){
            CommentaireDAO c = new CommentaireDAO();
            c.deleteCommentaire(id);
        }
        if (source.compareTo("destination")==0){
           DestinationDAO d = new DestinationDAO();
           d.deleteDestination(id);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

