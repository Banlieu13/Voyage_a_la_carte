    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
         contenue.setEditable(false);
        GererAlerteDAO gad = new GererAlerteDAO();
        int id = gad.getID();
        String l;
        String source = gad.getSource();
        System.out.println(id);
        System.out.println(source);
        if (source.compareTo("offre")==0){
           l= gad.AfficherOffre(id);
           contenue.setText(l);
        }
        
        if (source.compareTo("annonce")==0){
            l= gad.AfficherAnnonce(id);
            contenue.setText(l);
        }
        if (source.compareTo("commentaire")==0){
            l= gad.AfficherCommentaire(id);
            contenue.setText(l);
        }
        if (source.compareTo("destination")==0){
            l= gad.AfficherDestination(id);
            contenue.setText(l);
        }
    }//GEN-LAST:event_formWindowOpened

