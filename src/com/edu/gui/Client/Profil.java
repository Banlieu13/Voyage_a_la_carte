         tfnom.setText(c.getNom() +" " +c.getPrenom());
         ImageIcon ima = new ImageIcon();
         ima = cd.chercherImageClient(a.identifiant);
         pic.setIcon(ima);
         jlclient.setText(c.getNom()+" "+c.getPrenom());
        
