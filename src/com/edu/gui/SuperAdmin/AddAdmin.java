    private void btnadd1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnadd1ActionPerformed
        if ((tfmail1.getText().isEmpty())||(tfcin1.getText().isEmpty())||(tfnom1.getText().isEmpty())||(tfprenom1.getText().isEmpty())||(tfpassword1.getPassword().toString().isEmpty())||(tfville1.getText().isEmpty())) {
            res1.setText("Veuillez saisir toutes les champs");
        }
        else
        {
            Administrateur a = new Administrateur();
            AdminDAO adao = new AdminDAO();
            a.setEmail(tfmail1.getText());
            a.setCin(tfcin1.getText());
            a.setNom(tfnom1.getText());
            a.setPrenom(tfprenom1.getText());
            a.setVille(tfville1.getText());
            a.setPassword(tfpassword1.getText());
            adao.insertAdmin(a);
            res1.setText("Ajout avec succée");
        }
    }//GEN-LAST:event_btnadd1ActionPerformed

