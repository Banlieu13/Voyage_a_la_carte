    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        TableAdmin ta = new TableAdmin();
        int x = jTable1.getSelectedRow();
        if(x==-1){
            msg.setText("Veuillez sélectioné un Responsable");
        }
        else
        {
            String mail = ta.getValueAt(x, 0).toString();
            System.out.println(mail);
            new AdminDAO().deleteAdmin(mail);
            ta.listAdmin = new AdminDAO().AfficherAdmin();
            jTable1.setModel(ta);
            msg.setText("Suppression effectuée avec succée");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

